`Para resolver esse problema utilizando apenas duas idas até uma das salas das lâmpadas, podemos seguir estes passos:

Primeira ida à sala das lâmpadas:
Ligue o primeiro interruptor e aguarde alguns minutos.
Desligue o primeiro interruptor e ligue o segundo interruptor.
Entre na sala das lâmpadas.
Agora, ao entrar na sala das lâmpadas, você verá que:

Se a lâmpada estiver acesa, então o segundo interruptor controla essa lâmpada.
Se a lâmpada estiver apagada e ainda estiver fria (não aquecida), então o primeiro interruptor controla essa lâmpada.
Se a lâmpada estiver apagada, mas estiver quente (já foi ligada e desligada anteriormente), então o terceiro interruptor controla essa lâmpada.
Segunda ida à sala das lâmpadas:
Agora, sabendo qual interruptor controla qual lâmpada, você pode desligar todos os interruptores.
Ligue apenas o interruptor que controla a lâmpada que estava apagada e fria na primeira ida.
Dessa forma, ao entrar na sala das lâmpadas pela segunda vez, você poderá confirmar se identificou corretamente qual interruptor controla cada lâmpada, pois ao ligar o interruptor correto, a lâmpada que estava apagada e fria na primeira ida deve acender.`